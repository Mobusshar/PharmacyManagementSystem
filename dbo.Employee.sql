CREATE TABLE [dbo].[Employee]
(
	[Name] VARCHAR(50) NOT NULL PRIMARY KEY, 
    [ID] INT NULL, 
    [Department] VARCHAR(50) NULL, 
    [Address] VARCHAR(250) NULL, 
    [DOB] VARCHAR(50) NULL, 
    [Gender] VARCHAR(50) NULL, 
    [Salary] VARCHAR(50) NULL, 
    [Email] VARCHAR(50) NULL, 
    [Phone] VARCHAR(50) NULL
)
